package com.aoc2021.day3;

class BitCalculus {
    private int zeroBits;
    private int oneBits;

    public BitCalculus(int zeroBits, int oneBits) {
        this.zeroBits = zeroBits;
        this.oneBits = oneBits;
    }

    public int getZeroBits() {
        return zeroBits;
    }

    public int getOneBits() {
        return oneBits;
    }
}
